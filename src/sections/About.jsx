import { useRef } from "react";
import AnimatedHeaderSection from "../components/AnimatedHeaderSection";
import { AnimatedTextLines } from "../components/AnimatedTextLines";
import { useGSAP } from "@gsap/react";
import gsap from "gsap";

const About = () => {
  const text = `Assistant Professor and cloud security practitioner
    I design secure, scalable systems
    and mentor students and teams`;
  const aboutText = `I’m Aishwarya Anand Arora — Assistant Professor with experience at IITM College (Janak Puri) and GD Goenka University.
  I teach Data Structures & Algorithms, Web Technologies, C Programming and Business Communication. My technical focus includes AWS infrastructure (EC2, IAM, CloudFront, CloudWatch), enterprise Java development, and SQL-driven reporting.
  I’ve coordinated ERP systems, trained students for placements, and mentored communication and personality development. I enjoy simplifying complex topics and building reliable systems that scale.`;
  const imgRef = useRef(null);
  useGSAP(() => {
    gsap.to("#about", {
      scale: 0.95,
      scrollTrigger: {
        trigger: "#about",
        start: "bottom 80%",
        end: "bottom 20%",
        scrub: true,
        markers: false,
      },
      ease: "power1.inOut",
    });

    gsap.set(imgRef.current, {
      clipPath: "polygon(0 100%, 100% 100%, 100% 100%, 0% 100%)",
    });
    gsap.to(imgRef.current, {
      clipPath: "polygon(0% 0%, 100% 0%, 100% 100%, 0% 100%)",
      duration: 2,
      ease: "power4.out",
      scrollTrigger: { trigger: imgRef.current },
    });
  });
  return (
    <section id="about" className="min-h-screen bg-black rounded-b-4xl">
      <AnimatedHeaderSection
        subTitle={"Educator • Cloud Infrastructure • Java"}
        title={"About"}
        text={text}
        textColor={"text-white"}
        withScrollTrigger={true}
      />
      <div className="flex flex-col items-center justify-between gap-16 px-10 pb-16 text-xl font-light tracking-wide lg:flex-row md:text-2xl lg:text-3xl text-white/60">
        <img
          ref={imgRef}
          src="/images/aishwarya.jpeg"
          alt="Aishwarya Anand Arora"
          className="w-md rounded-3xl"
        />
        <AnimatedTextLines text={aboutText} className={"w-full"} />
      </div>
    </section>
  );
};

export default About;
